import * as Types from "./types";

const InitialState = {
  propertyData: {},
  loading: false,
};

const ProjectPropertyReducer = (state = InitialState, action) => {
  switch (action.type) {
    case Types.PROJECT_PROPERTY_REQUEST:
      return {
        propertyData: {},
        loading: true,
      };

    case Types.PROJECT_PROPERTY_SUCCESS:
      return {
        propertyData: action.payload,
        loading: false,
      };

    case Types.PROJECT_PROPERTY_FAILED:
      return {
        propertyData: {},
        loading: false,
      };

    default:
      return state;
  }
};

export default ProjectPropertyReducer;
